/**
 * Generate releases-data.ts from git history.
 * Parses conventional commits (feat:, fix:, security:) between tags.
 *
 * Usage: node scripts/gen-releases.mjs
 */
import { execSync } from "node:child_process";
import { writeFileSync, readFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const OUT = join(ROOT, "src", "components", "navigation", "releases-data.ts");

function git(cmd) {
  return execSync(`git ${cmd}`, { cwd: ROOT, encoding: "utf-8", shell: true }).trim();
}

function getTags() {
  const raw = git("tag -l");
  if (!raw) return [];
  // Normalize tags (remove 'v' prefix) and deduplicate
  const tags = raw.split("\n").filter(Boolean);
  const uniqueTags = new Set();
  const result = [];
  for (const t of tags) {
    const norm = t.replace(/^v/, "");
    if (!uniqueTags.has(norm)) {
      uniqueTags.add(norm);
      result.push(t); // keep original format for git log
    }
  }
  // Sort by semantic version descending (not lexicographic)
  result.sort((a, b) => {
    const va = a.replace(/^v/, "").split(".").map(Number);
    const vb = b.replace(/^v/, "").split(".").map(Number);
    for (let i = 0; i < 3; i++) {
      if ((vb[i] || 0) !== (va[i] || 0)) return (vb[i] || 0) - (va[i] || 0);
    }
    return 0;
  });
  return result;
}

function getCommitsBetween(from, to) {
  const range = from ? `${from}..${to}` : to;
  const raw = git(`log ${range} --pretty=format:"%H~%s~%aI" --no-merges`);
  if (!raw) return [];
  return raw.split("\n").filter(Boolean).map((line) => {
    const [hash, subject, date] = line.split("~");
    return { hash, subject, date };
  });
}

function parseCommit(subject) {
  // feat: ..., fix: ..., security: ..., chore: ..., docs: ...
  const match = subject.match(/^(feat|fix|security|chore|docs|refactor|perf|test)(?:\(.+?\))?:\s*(.+)$/i);
  if (!match) return null;
  const kind = match[1].toLowerCase();
  const text = match[2].trim();
  // Skip release commits
  if (kind === "chore" && text.startsWith("release")) return null;
  if (text.startsWith("release:")) return null;
  // Map non-highlight kinds
  if (kind === "chore" || kind === "docs" || kind === "refactor" || kind === "perf" || kind === "test") return null;
  return { kind, text };
}

function formatDate(isoDate) {
  const d = new Date(isoDate);
  const months = ["janeiro","fevereiro","março","abril","maio","junho","julho","agosto","setembro","outubro","novembro","dezembro"];
  return `${months[d.getMonth()]} ${d.getFullYear()}`;
}

// ── Main ─────────────────────────────────────────────────────────────────────

const tags = getTags();
const releases = [];

// Collect commits for each tag
for (let i = 0; i < tags.length; i++) {
  const tag = tags[i];
  const prevTag = tags[i + 1] || null;
  const version = tag.replace(/^v/, "");
  const commits = getCommitsBetween(prevTag, tag);
  const highlights = [];

  for (const c of commits) {
    const parsed = parseCommit(c.subject);
    if (parsed) highlights.push(parsed);
  }

  if (highlights.length > 0) {
    // Use date from the most recent commit in this range
    const date = commits.length > 0 ? formatDate(commits[0].date) : "";
    releases.push({ version, date, highlights });
  }
}

// Also collect unreleased commits (since last tag)
const lastTag = tags[0] || null;
if (lastTag) {
  const unreleased = getCommitsBetween(lastTag, "HEAD");
  const highlights = [];
  for (const c of unreleased) {
    const parsed = parseCommit(c.subject);
    if (parsed) highlights.push(parsed);
  }
  if (highlights.length > 0) {
    // Get current version from package.json
    const pkg = JSON.parse(readFileSync(join(ROOT, "package.json"), "utf-8")).version;
    releases.unshift({ version: pkg, date: "em desenvolvimento", highlights });
  }
}

// If no tags exist, collect all commits
if (tags.length === 0) {
  const all = getCommitsBetween(null, "HEAD");
  const highlights = [];
  for (const c of all) {
    const parsed = parseCommit(c.subject);
    if (parsed) highlights.push(parsed);
  }
  const pkg = JSON.parse(readFileSync(join(ROOT, "package.json"), "utf-8")).version;
  releases.push({ version: pkg, date: "em desenvolvimento", highlights });
}

// Always ensure at least one entry so RELEASES[0] is never undefined
if (releases.length === 0) {
  const pkg = JSON.parse(readFileSync(join(ROOT, "package.json"), "utf-8")).version;
  releases.push({ version: pkg, date: "", highlights: [] });
}

// Deduplicate highlights within each release (same text)
for (const rel of releases) {
  const seen = new Set();
  rel.highlights = rel.highlights.filter((h) => {
    const key = h.text.toLowerCase();
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

// Write output
const output = `// Auto-generated by scripts/gen-releases.mjs — DO NOT EDIT
// Run: node scripts/gen-releases.mjs

export interface ReleaseHighlight {
  kind: "feat" | "fix" | "security";
  text: string;
}

export interface Release {
  version: string;
  date: string;
  highlights: ReleaseHighlight[];
}

export const RELEASES: Release[] = ${JSON.stringify(releases, null, 2)};
`;

writeFileSync(OUT, output, "utf-8");
console.log(`✓ Generated ${OUT} with ${releases.length} release(s)`);
for (const r of releases) {
  console.log(`  v${r.version} — ${r.highlights.length} highlights`);
}
